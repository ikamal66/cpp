#include <iostream>
using namespace std;

int main()
{
    int a , b ;
cout<<"Enter the value of a "<<endl;
cin>>a;
cout<<"Enter the value of b "<<endl;
cin>>b;


    cout << "The value of a + b is " << a + b << endl;
    cout << "The value of a - b is " << a - b << endl;
    cout << "The value of a * b is " << a * b << endl;
    cout << "The value of a / b is " << a / b << endl;
    cout << "The value of a % b is " << a % b << endl;
    cout << "The value of a ++ is " << a++ << endl;
    cout << "The value of a-- is " << a-- << endl;
    cout << "The value of ++a  is " << ++a << endl;
    cout << "The value of --a is " << --a << endl;
    cout<<endl;

    return 0;
        
}